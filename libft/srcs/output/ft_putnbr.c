#include "libft.h"

void	ft_putnbr(int nb)
{
	unsigned int n;

	n = nb;
	if (nb < 0)
	{
		n *= -1;
		ft_putchar('-');
	}
	if (n / 10)
		ft_putnbr(n / 10);
	ft_putchar('0' + (n % 10));
}
