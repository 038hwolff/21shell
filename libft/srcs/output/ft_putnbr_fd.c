#include "libft.h"

void	ft_putnbr_fd(int nb, int fd)
{
	unsigned int n;

	n = nb;
	if (nb < 0)
	{
		n *= -1;
		ft_putchar_fd('-', fd);
	}
	if (n > 9)
		ft_putnbr_fd(n / 10, fd);
	ft_putchar_fd('0' + (n % 10), fd);
}
