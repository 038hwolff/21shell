#include "libft.h"

size_t	ft_wrdcnt(const char *s, char c)
{
	size_t	res;

	if (!s)
		return (0);
	res = 1;
	while (*s == ' ')
		++s;
	while (*s)
	{
		if (*s == c && *(s + 1) != c && *(s + 1))
			++res;
		++s;
	}
	return (res);
}
