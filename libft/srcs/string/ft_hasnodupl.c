int	ft_hasnodupl(char *str)
{
	char *str2;

	while (*str)
	{
		str2 = str + 1;
		while (*str2)
		{
			if (str != str2 && *str == *str2)
				return (0);
			++str2;
		}
		++str;
	}
	return (1);
}
