#include "libft.h"

char	*ft_strdup(const char *s1)
{
	size_t	len;
	char	*dup;

	len = ft_strlen(s1);
	if (!(dup = ft_strnew(len)))
		return (NULL);
	ft_strcpy(dup, s1);
	return (dup);
}
